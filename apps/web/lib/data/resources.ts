export function getResourcesByCategory(category: ResourceCategory): ResourceItem[] {
  return ALL_RESOURCES.filter((r) => r.category === category);
}

export function getResourceBySlug(slug: string): ResourceItem | undefined {
  return ALL_RESOURCES.find((r) => r.slug === slug);
}

export function getFeaturedResources(): ResourceItem[] {
  return ALL_RESOURCES.filter((r) => r.featured);
}

export function getResourceSubcategories(category: ResourceCategory): string[] {
  const items = getResourcesByCategory(category);
  return [...new Set(items.map((r) => r.subcategory))].sort();
}

export function searchResources(query: string): ResourceItem[] {
  const lower = query.toLowerCase();
  return ALL_RESOURCES.filter(
    (r) =>
      r.title.toLowerCase().includes(lower) ||
      r.description.toLowerCase().includes(lower) ||
      r.tags.some((t) => t.includes(lower)) ||
      r.subcategory.toLowerCase().includes(lower),
  );
}
